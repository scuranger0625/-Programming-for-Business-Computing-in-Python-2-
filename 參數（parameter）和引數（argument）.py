def greet(name, age):
    """
    這個函數接受兩個參數：name 和 age。
    """
    # name 和 age 是參數（parameters）
    print(f"Hello, my name is {name} and I am {age} years old.")

# 呼叫函數 greet 並傳遞引數
greet("Alice", 30)  # "Alice" 和 30 是引數（arguments）


